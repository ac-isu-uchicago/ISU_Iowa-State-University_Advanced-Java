package chapter_01;

/**
 * (Area and perimeter of a rectangle) Write a program that displays the area and
 * perimeter of a rectangle with the width of 4.5 and height of 7.9 using the following
 * formula:
 *
 *      area = width * height
 */
public class PE_01_09_Area_And_Perimeter_Of_A_Rectangle {
    public static void main(String[] args) {
        System.out.println("Perimeter = " + (4.5 + 4.5 + 7.9 + 7.9));
        System.out.println("area = " + 4.5 * 7.9);
    }
}
